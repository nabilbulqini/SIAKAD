    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String kode = txtKodeDosen.getText().trim();

    if (kode.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Pilih data dosen yang akan dihapus!",
            "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(this,
        "Hapus dosen dengan kode: " + kode + "?\nData tidak bisa dikembalikan!",
        "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        boolean berhasil = ds.hapusDosen(kode);
        if (berhasil) {
            JOptionPane.showMessageDialog(this, "Data dosen berhasil dihapus!");
            refreshTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this,
                "Gagal menghapus! Kode dosen tidak ditemukan.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

