    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:
         String kode = txtKodeDosen.getText().trim();
         String nama = txtNamaDosen.getText().trim();
         // Ambil jabatan dari combobox
         String jabatanStr = cmbJabatan.getSelectedItem().toString();
         JabatanDosen jabatan = JabatanDosen.valueOf(jabatanStr); // konversi ke enum

    if (kode.isEmpty() || nama.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Kode Dosen dan Nama tidak boleh kosong!",
            "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (ds.findDosenByKode(kode) != null) {
        JOptionPane.showMessageDialog(this,
            "Kode Dosen " + kode + " sudah terdaftar!",
            "Duplikat", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Dosen d = new Dosen(kode, nama, jabatan);
    boolean berhasil = ds.tambahDosen(d);

    if (berhasil) {
        JOptionPane.showMessageDialog(this, "Data dosen berhasil ditambahkan!");
        refreshTable();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this,
            "Data penuh! Maksimal " + DataStore.MAX + " dosen.",
            "Penuh", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnInputActionPerformed

