        private void updateSKS() {
        DataStore ds = DataStore.getInstance();
        int idx = cmbMahasiswa.getSelectedIndex();

        if (idx < 0 || idx >= ds.getJumlahMhs()) return;

        String nim   = ds.getMahasiswas()[idx].getNim();
        int total    = ds.getTotalSKS(nim);
        int sisa     = 24 - total;

        // Update label
        lblTotalSKS.setText("Total SKS: " + total + " / 24  (sisa " + sisa + " SKS)");

         // Update progress bar
         progressSKS.setValue(total);
         progressSKS.setString(total + " / 24 SKS");

         // Warna progress bar sesuai kondisi
         if (total >= 22) {
         // Hampir penuh — merah
         progressSKS.setForeground(new java.awt.Color(231, 76, 60));
         lblTotalSKS.setForeground(new java.awt.Color(231, 76, 60));
    } else if (total >= 18) {
         // Sedang — orange
         progressSKS.setForeground(new java.awt.Color(230, 126, 34));
         lblTotalSKS.setForeground(new java.awt.Color(230, 126, 34));
    } else {
         // Aman — biru
         progressSKS.setForeground(new java.awt.Color(46, 134, 171));
         lblTotalSKS.setForeground(new java.awt.Color(46, 134, 171));
    }
}
    
    private void refreshTable() {
    model.setRowCount(0);
    for (int i = 0; i < ds.getJumlahKRS(); i++) {
        KRS k = ds.getKrsList()[i];
        model.addRow(new Object[]{
            k.getIdKRS(),
            k.getMahasiswa().getNama(),
            k.getMataKuliah().getNamaMK(),
            k.getMataKuliah().getSks(),
            k.getDosen().getNama(),
            k.getSemester()
        });
    }
}
    
