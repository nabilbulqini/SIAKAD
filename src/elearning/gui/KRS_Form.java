    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:
        DataStore ds = DataStore.getInstance();

    int idxMhs = cmbMahasiswa.getSelectedIndex();
    int idxMK  = cmbMatakuliah.getSelectedIndex();
    int idxDsn = cmbDosen.getSelectedIndex();

    if (idxMhs < 0 || idxMK < 0 || idxDsn < 0) {
        JOptionPane.showMessageDialog(this, "Semua field harus dipilih!",
            "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String semester = txtSemester.getText().trim();
    if (semester.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semester tidak boleh kosong!");
        return;
    }

    Mahasiswa  mhs   = ds.getMahasiswas()[idxMhs];
    MataKuliah mk    = ds.getMataKuliahs()[idxMK];
    Dosen      dosen = ds.getDosens()[idxDsn];

    // ── Validasi total SKS ≤ 24 ──────────────────────────────
    int sksSudah   = ds.getTotalSKS(mhs.getNim());
    int sksTambah  = mk.getSks();
    int totalNanti = sksSudah + sksTambah;

    if (totalNanti > 24) {
        // Animasikan progress bar ke merah dulu
        progressSKS.setValue(24);
        progressSKS.setForeground(new java.awt.Color(231, 76, 60));
        progressSKS.setString("MELEBIHI BATAS!");

        JOptionPane.showMessageDialog(this,
            "Batas SKS Terlampaui!\n\n"
            + "SKS sudah diambil    : " + sksSudah + " SKS\n"
            + "SKS mata kuliah ini  : " + sksTambah + " SKS\n"
            + "Total jika ditambah  : " + totalNanti + " SKS\n\n"
            + "Maksimal 24 SKS per semester.",
            "Batas SKS Terlampaui", JOptionPane.ERROR_MESSAGE);

        updateSKS(); // kembalikan progress bar ke kondisi asli
        return;
    }

    // ── Simpan KRS ────────────────────────────────────────────
    String idKRS = "KRS" + String.format("%03d", ++idKRSCounter);
    KRS krs = new KRS(idKRS, mhs, mk, dosen, semester);

    boolean berhasil = ds.tambahKRS(krs);
    if (berhasil) {
        refreshTable();
        updateSKS(); // update progress bar setelah input

        JOptionPane.showMessageDialog(this,
            "KRS berhasil disimpan!\n"
            + "Total SKS " + mhs.getNama() + " sekarang: "
            + ds.getTotalSKS(mhs.getNim()) + " SKS");
    } else {
        JOptionPane.showMessageDialog(this,
            "Data KRS penuh! Maksimal " + DataStore.MAX + " KRS.",
            "Penuh", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnInputActionPerformed

