    private void refreshTable() {
    model.setRowCount(0);
    for (int i = 0; i < ds.getJumlahMK(); i++) {
        MataKuliah m = ds.getMataKuliahs()[i];
        model.addRow(new Object[]{m.getKodeMK(), m.getNamaMK(), m.getSks()});
    }
}

