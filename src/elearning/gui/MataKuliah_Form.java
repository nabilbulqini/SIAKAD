    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:
         String kode = txtKodeMK.getText().trim();
    String nama = txtNamaMK.getText().trim();
    int sks     = (Integer) spnSKS.getValue();

    if (kode.isEmpty() || nama.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Kode MK dan Nama MK tidak boleh kosong!",
            "Validasi", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (ds.findMKByKode(kode) != null) {
        JOptionPane.showMessageDialog(this,
            "Kode MK " + kode + " sudah terdaftar!",
            "Duplikat", JOptionPane.WARNING_MESSAGE);
        return;
    }

    MataKuliah mk = new MataKuliah(kode, nama, sks);
    boolean berhasil = ds.tambahMataKuliah(mk);

    if (berhasil) {
        JOptionPane.showMessageDialog(this, "Mata kuliah berhasil ditambahkan!");
        refreshTable();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this,
            "Data penuh! Maksimal " + DataStore.MAX + " mata kuliah.",
            "Penuh", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnInputActionPerformed

