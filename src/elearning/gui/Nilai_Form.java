    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        hitungNilai();

    // Cek apakah sudah ada nilai (lblNilaiAkhir tidak "—")
    if (lblNilaiAkhir.getText().equals("—")) return;

    try {
        int idx = cmbKRS.getSelectedIndex();
        if (idx < 0) {
            JOptionPane.showMessageDialog(this, "Pilih KRS terlebih dahulu!");
            return;
        }

        KRS krs     = ds.getKrsList()[idx];
        double uts     = parseInput(txtUTS,     "UTS");
        double tugas   = parseInput(txtTugas,   "Tugas");
        double absensi = parseInput(txtAbsensi, "Absensi");
        double uas     = parseInput(txtUAS,     "UAS");

        Nilai n = new Nilai(krs, uts, tugas, absensi, uas);
        boolean berhasil = ds.tambahNilai(n);

        if (berhasil) {
            JOptionPane.showMessageDialog(this,
                "✅ Nilai berhasil disimpan!\n"
                + "Nilai Akhir : " + String.format("%.2f", n.getNilaiAkhir())
                + " (" + n.getNilaiHuruf() + ")");
            refreshTable();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this,
                "Data nilai penuh! Maksimal " + DataStore.MAX + " nilai.",
                "Penuh", JOptionPane.ERROR_MESSAGE);
        }

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Perbaiki input terlebih dahulu!",
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

