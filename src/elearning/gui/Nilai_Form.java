private double parseInput(javax.swing.JTextField tf, String namaField) {
    String text = tf.getText().trim();
    if (text.isEmpty()) {
        throw new IllegalArgumentException("Nilai " + namaField + " tidak boleh kosong!");
    }
    try {
        double val = Double.parseDouble(text);
        if (val < 0 || val > 100) {
            throw new IllegalArgumentException(
                "Nilai " + namaField + " harus antara 0 - 100!");
        }
        return val;
    } catch (NumberFormatException e) {
        throw new IllegalArgumentException(
            "Nilai " + namaField + " harus berupa angka!");
    }
}

private void refreshTable() {
    model.setRowCount(0);
    for (int i = 0; i < ds.getJumlahNilai(); i++) {
        Nilai n = ds.getNilais()[i];
        model.addRow(new Object[]{
            n.getKrs().getMahasiswa().getNama(),
            n.getKrs().getMataKuliah().getNamaMK(),
            n.getNilaiUTS(),
            n.getNilaiTugas(),
            n.getNilaiAbsensi(),
            n.getNilaiUAS(),
            String.format("%.2f", n.getNilaiAkhir()),
            n.getNilaiHuruf()
        });
    }
}

private void hitungNilai() {
    try {
        double uts     = parseInput(txtUTS,     "UTS");
        double tugas   = parseInput(txtTugas,   "Tugas");
        double absensi = parseInput(txtAbsensi, "Absensi");
        double uas     = parseInput(txtUAS,     "UAS");

        int idx = cmbKRS.getSelectedIndex();
        if (idx < 0) return;
        KRS krs = ds.getKrsList()[idx];

        Nilai n = new Nilai(krs, uts, tugas, absensi, uas);

        lblNilaiAkhir.setText(String.format("%.2f", n.getNilaiAkhir()));
        lblNilaiHuruf.setText(n.getNilaiHuruf());

        switch (n.getNilaiHuruf()) {
            case "A": lblNilaiHuruf.setForeground(java.awt.Color.GREEN.darker()); break;
            case "B": lblNilaiHuruf.setForeground(java.awt.Color.BLUE); break;
            case "C": lblNilaiHuruf.setForeground(java.awt.Color.ORANGE); break;
            default:  lblNilaiHuruf.setForeground(java.awt.Color.RED); break;
        }

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, e.getMessage(),
            "Input Tidak Valid", JOptionPane.ERROR_MESSAGE);
    }
}

